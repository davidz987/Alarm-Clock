    func application(_ application: UIApplication, didFinishLaunchingWithOptions launchOptions: [UIApplication.LaunchOptionsKey: Any]?) -> Bool {
        
        //assigns delegates to deal with notifs in foreground
        UNUserNotificationCenter.current().delegate = self
        
        //Requests permission for notifications
        let center = UNUserNotificationCenter.current()
        center.requestAuthorization(options: [.alert, .sound]) { (granted, error) in
            // Enable or disable features based on authorization.
        }
        
        //Adds an action to open the app when notification pushed
        let open_app = UNNotificationAction(identifier: "Open_app",
                                              title: "Stop",
                                              options: .foreground)
        
        //Sets category of notification
        let generalCategory = UNNotificationCategory(identifier: "Alarm_going_off",
                                                     actions: [open_app],
                                                     intentIdentifiers: [],
                                                     options: .customDismissAction)
        
        // Register the category.
        center.setNotificationCategories([generalCategory])
        
        // Override point for customization after application launch.
        return true
    }
    
